    private void Add_student_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_student_btnActionPerformed
        myCon = DBHelper.connectDB();
        String sql_insert = "INSERT INTO students (student_num,first_name,last_name)VALUES(?,?,?);";
        try {
            ps = myCon.prepareStatement(sql_insert);
            ps.setInt(1, Integer.parseInt(student_num_txt.getText().trim()));
            ps.setString(2, first_name_txt.getText().trim());
            ps.setString(3, last_name_txt.getText().trim());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "New student successfully added.");
            student_num_txt.setText("");
        } catch (SQLException ex) {
            System.out.println("error");
          //  Logger.getLogger(University.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Add_student_btnActionPerformed

