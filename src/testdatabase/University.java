    private void show_edit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_edit_btnActionPerformed
        String query = "SELECT first_name, last_name from students where student_num=?";
        myCon = DBHelper.connectDB();
        try {
            ps = myCon.prepareStatement(query);
            ps.setInt(1, Integer.parseInt(student_num_edit_txt.getText().trim()));
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                first_name_edit_txt.setText(rs.getString("first_name"));
                last_name_edit_txt.setText(rs.getString("last_name"));
                
            }
            
        } catch (SQLException ex) {
          //  Logger.getLogger(University.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("error");
        }
        
    }//GEN-LAST:event_show_edit_btnActionPerformed

